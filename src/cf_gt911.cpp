void FT8xx_Init_Goodix_GT911(void)
{
	//Sets up address, writes the data blob, and executes
	DPRINT("FT8xx-GT911 SETUP\n");

	FT81x_WriteCommandBuffer((uint8_t*)Goodix_GT911_Init_Data, GOODIX_GT911_INIT_DATA_LENGTH);

	//"Straight AN_336"
	//"Hold the touch engine in reset (write REG_CPURESET=2)"
	FT81x_W8(REG_CPU_RESET + RAM_REG, 0x02);

	//"Write REG_TOUCH_CONFIG=0x05D0"
	FT81x_W16(REG_TOUCH_CONFIG + RAM_REG, 0x05D0);

	//"Set GPIO0 output LOW"
	// The CFA10100 FT813 uses GPIO3 to reset GT911
	// Reset-Value is 0x8000 adding 0x0008 sets GPIO3 to output
	// Default-value for REG_GPIOX is 0x8000 -> Low output on GPIO3
	FT81x_W16(REG_GPIOX_DIR + RAM_REG, 0x8008);
	// "Wait more than 100us"
	delay(2);
	// "Write REG_CPURESET=0"
	FT81x_W8(REG_CPU_RESET + RAM_REG, 0x00);
	// "Wait more than 55ms"
	delay(60);
	// "Set GPIO0 to input (floating)"
	// The CFA10100 FT813 uses GPIO3 to reset GT911
	FT81x_W16(REG_GPIOX_DIR + RAM_REG, 0x8000);

	//done
	DPRINT("FT8xx-GT911 SETUP DONE\n");
}

